                    <h2> Web Scraping: Portal Transparência do Estado do Rio Grande do Norte</h2>
                    <time class=container-time>
                        <div class="container-img">
                            <img src="medias/calendar.svg" alt="Calendário">
                        </div>    
                        21/01/2020
                    </time>
                    <article>
                        <p>
                            Nos dias de hoje fala-se muito sobre a alta produção de dados diariamente, o que torna os campos de big data e data science 
                            tão importantes. Contudo, quem está fora do processo não enxerga um dos principais passos e assunto do trabalho de hoje, 
                            a extração dos dados.
                        </p>
                        <p>
                            Grandes empresas possuem enormes bancos de dados bem estruturados, claro que também possuem os seus problemas, mas não têm 
                            muitas dificuldades em adquirir dados. Por outro lado, microempresas ou até pessoas físicas necessitam de dados, mas se não 
                            houver recurso financeiro suficientemente grande, não conseguem ter dados de qualidade.
                        </p>
                        <p>
                            Uma das ferramentas mais poderosas na extração de dados é o <i>web scraping</i>, ou raspagem web, que por definição é uma forma
                            de mineração que permite a extração de dados de sites da web convertendo-os em informação estruturada para posterior análise.
                        </p>
                        <p>
                            Na linguagem Python existem alguns pacotes que executam muito bem o trabalho de raspagem, como o <i>Beautiful Soup</i>, contudo 
                            a diversidade de linguagem de programação de sites é bastante diversa, tornando cada site com uma particularidade diferente para
                            a raspagem. Apesar de não ser o mais indicado para a raspagem de dados, utilizaremos o <i>Selenium</i> e não o <i>Beautiful Soup</i>.
                            Com certeza existem outros métodos para executar a tarefa que apresentaremos, talvez até mais eficazes, mas este foi um método efetivo 
                            para a tarefa que estávamos planejando.
                        </p>
                        <p>
                            O <i>Selenium</i> é um pacote bastante utilizado para automação de tarefas no <i>web browser</i>. A utilização dele foi necessária, 
                            pois o <a href="http://servicos.searh.rn.gov.br/searh/Remuneracao/RemuneracaoPorId/17301247?MesAno=10%2F2019"><i>web site</i></a> que 
                            estamos raspando possui uma primeira página de filtragem dos dados requisitados e em seguida a página com os dados. Se o filtro 
                            que escolhermos apresentar dados que não cabem somente na primeira página, que é o nosso caso, haverá um índice com os números das próximas 
                            páginas. O problema em se usar o <i>Beautiful Soup</i> é que ele acessa os dados através da <i>URL</i> e neste caso não é possível acessar
                            cada página apenas alterando o número da página na <i>URL</i>. Então a solução encontrada foi automatizar uma tarefa que raspasse os dados de 
                            uma página e depois clicasse no botão "próxima", para ir para a próxima página e, então, raspar os dados novamente, até a última página.

                        </p>
                        <h3>O <i>web site</i></h3>
                        <p>
                            O <i>site</i> que foi escolhido para a raspagem é o "Portal da Transparência do Estado do Rio Grande do Norte", deste <a href="http://servicos.searh.rn.gov.br/searh/Remuneracao">link</a>. Os filtros
                            aplicados nestre trabalho foram: mês de Outubro, ano de 2019, pesquisando por órgão e selecionamos por acaso a Secretária da Tributação. Abaixo está a página de pesquisa dos dados com os filtros inseridos.
                            <img src="medias/web_scraping_rn/filtros.png" alt="Filtros utilizados">
                        </p>
                        <p>
                            A primeira página com os dados é esta a seguir. Se você reparar na <i>URL</i> poderá ver que os filtros aplicados estão descritos nela e que não há nenhum índice referenciado a página.

                            <img src="medias/web_scraping_rn/primeira_pagina.png" alt="Primeira página">
                            
                        </p>
                        <p>
                            Quando verificamos a segunda página abaixo, é possível confirmar que não há um sequenciamento da página anterior. Caso copiemos e colemos esta mesma <i>URL</i> na barra de endereços do navegador, haverá o
                            redirecionamento para a página inicial, na qual seleciona-se os filtros. Teste você mesmo!
                            <img src="medias/web_scraping_rn/segunda_pagina.png" alt="Segunda página">
                        </p>
                        
                        <p>
                            Por este motivo, foi necessário utilizar da ferramenta poderosa que é o <i>Selenium</i>, pois a partir da primeira página, após a raspagem dos dados, o automatizador de tarefas clica no botão de próxima página 
                            até encontrar a página que não possui este botão, inferindo que esta seja a última página. No vídeo abaixo você pode verificar todo o processo realizado pelo <i>Selenium</i>.
                            
                        </p>
