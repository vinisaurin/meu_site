                        <h3><i>Data Visualization</i> das corridas de Uber</h3>
                        <p>
                            O presente trabalho visa apresentar uma série de visualizações dos dados relativos às corridas de Uber nos meses de Jul/14 e Set/14. Os dados foram
                            retirados do <a href="https://www.kaggle.com/fivethirtyeight/uber-pickups-in-new-york-city">Kaggle</a> e apresenta cerca de 1,7 milhões de corridas
                            nestes dois mês analisados. O conjunto de dados consiste no horário da corrida e na latitude e longitude do local em que a corrida iniciou.
                        </p>
                        <p>
                            Abaixo está o mapa de calor com todas as corridas feitas no mês de Julho de 2014. Como era de se esperar a maior concentração de eventos está na ilha
                            de Manhattan, em seguida pode-se encontrar um grande volume de corridas iniciadas no aeroporto internacional John F. Kennedy (JFK), assim como nos 
                            aeroportos La Guardia e de Newark em menores proporções.
                        </p>
                            <iframe src="medias/uber_pickups/geral_julho.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                        <p>
                            Para melhor entendermos o comportamento dos usuários do transporte por meio de aplicativos, dividimos os dados conforme o dia da semana e o horário do dia 
                            que ocorreu a corrida, pois de acordo com o gráfico abaixo há claramente uma sazonalidade em torno destas duas variáveis. 
                        </p>
                            <img src="medias/uber_pickups/heatmap.png" alt="Heatmap">
                        <p>
                            É importante também entendermos que Julho é o mês no qual a maioria dos norte-americanos tira férias e também é um período em que Nova York é muito procurada 
                            por turistas, enquanto que Setembro as aulas já retornam, o turismo diminui e os trabalhadores voltam aos seus ofícios e, por todos esses motivos, os gráficos de calor 
                            têm uma pequena diferença. Os horários de maiores picos de corridas em Setembro são nos dias mais próximos aos fins de semana e, em geral, são um pouco mais 
                            tarde em relação a Julho.
                        </p>
                        <p>
                            A seguir está novamente o mapa de Nova York, mas agora há marcações em determinados pontos do mapa que mostram alguns pontos de interesses (POI) selecionados para fazermos
                            análises posteriormente. Entre os POIs estão o <i>Empire State Building</i>, <i>Rockfeller Center</i>, <i>Central Station</i> e o aeroporto JFK. Para visualizar cada ponto
                            basta clicar nas respectivas marcas.
                        </p>
                            <iframe src="medias/uber_pickups/pontos_de_interesse.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                        <p>
                            A intenção deste projeto era apresentar a dinâmica das corridas no mapa conforme o horário, entretanto no momento em que escrevo este projeto o <i>plugin Heatmap with time</i>
                            do pacote <i>folium</i> está com problemas, portanto apresentaremos sem a dinâmica temporal. Para fazermos as análises apresentadas a seguir, delimitamos os locais de início das 
                            corridas em até 250 metros dos pontos de interesse listados no mapa anterior ou 1 km para os aeroportos e, para esse cálculo, foi utilizada a fórmula de <i>Haversine</i>. Para 
                            focarmos melhor em alguns POIs, traçamos algumas comparações:
                        </p>
                        <h4> Rockfeller Center x Empire State Building</h4>
                        <p>
                            Quase todo turista que vai a <i>'Big Apple'</i> fica com a dúvida se visita o observatório do <i>Empire State Building (ESB)</i> ou o <i>Top of the Rock</i>, no <i>Rockfeller Center</i>.
                            O primeiro possui 102 andares e 381 metros de altura (443 metros com a antena), enquanto que o segundo possui 70 andares, 259 metros de altura e é onde é gravado o programa 
                            <i>Saturday Night Live</i> da NBC. Os dois próximos mapas apresentam os períodos da manhã (05 às 12) e noite (18 às 05), respectivamente, nos dias da semana durante o mês de Julho.     
                        </p>
                            <iframe src="medias/uber_pickups/jul_manha_semana.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Julho/14 - Manhã - Semana</figcaption>
                        <p>
                            A partir destes gráficos é possível concluir que o <i>Rockfeller Center</i> possui uma área mais densa em relação ao ESB, entretanto é preciso fazer uma ressalva, pois o <i>Rockfeller Center</i> é
                            um complexo de 19 prédios comerciais que ocupa uma área de 89.000 m<sup style="font-size:60%;">2</sup> e, portanto, somente com os dados que temos não é possível concluir que o é o prédio mais 
                            visitado de Nova York, entretanto é possível afirmar que o ESB é o prédio mais famoso da cidade, e para vê-lo do melhor ponto de vista da cidade, é necessário ir ao <i>Rockfeller Center</i>.
                        </p>
                            <iframe src="medias/uber_pickups/jul_noite_semana.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Julho/14 - Noite - Semana</figcaption>
                        
                        <h4> JFK x La Guardia x Newark Airport</h4>
                        <p>
                            Por ser uma das principais cidades do mundo, NYC tem ao seu redor 3 grandes aeroportos, o <i>La Guardia</i> e o <i>JFK</i> que se localizam no <i>Queens</i>, além do Aeroporto Internacional de Newark.
                            Nos dois mapas a seguir, pode-se visualizar que entre os três o de menor movimentação em relação ao número de corridas é o de <i>Newark</i>, muito provavelmente por causa da sua maior distância para
                            <i>Manhattan</i>. Já o aerporto <i>La Guardia</i>, apesar de receber principalmente voos nacionais, é o que possui o maior volume de chamadas de carros por aplicativo e, semelhante ao que acontece com
                            o JFK, possui um volume ainda maior durante as noites dos dias de semana.
                        </p>


                            <iframe src="medias/uber_pickups/jul_manha_fds.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Julho/14 - Manhã - Fim de semana</figcaption>


                            <iframe src="medias/uber_pickups/jul_noite_fds.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Julho/14 - Noite - Fim de semana</figcaption>

                        <h4>Metropolitan Museum of Art x American Museum of Natural History</h4>

                            <p>
                                A cidade de NYC possui uma diversidade de lugares para se apreciar arte e cultura e 2 deles estão em lados opostos de um dos principais e mais belos parques do mundo, o <i>Central Park</i>. O museu de
                                história natural não precisa de apresentação, pois foi o local onde se passou a maior parte do filme "Uma noite no museu", enquanto que o <i>Metropolitan Museum of Art</i>, mais conhecido como <i>The Met</i>,
                                abriga uma importante coleção de pintura europeia dos séculos XII-XX e obras da arte antiga e oriental. 
                            </p>

                            <iframe src="medias/uber_pickups/sep_manha_fds.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Setembro/14 - Manhã - Fim de semana</figcaption>
                            <p>
                                É muito comum que os turistas visitem ambos os museus, entretanto, a partir dos dados que possuímos, o volume de pedidos de carros por aplicativo nas proximidades do museu de história natural é maior, embora
                                tenhamos que ressaltar que o perímetro desse museu coberto por ruas é bem maior que o <i>The Met</i>. Quando comparados os dias da semana, é evidente que nos dias úteis há maior movimentação em ambos os museus.
                            </p>
                            
                            
                            <iframe src="medias/uber_pickups/sep_manha_semana.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Setembro/14 - Manhã - Semana</figcaption>
                            <h4>Central Station</h4>
                            <p>
                                A <i>Grand Central Terminal</i> é considerada a maior estação ferroviária do mundo, com 44 plataformas, em dois níveis, com 41 linhas no nível superior e 26 no inferior. Em média, 125 mil usuários passam por el 
                                todos os dias. 
                            </p>
                            <iframe src="medias/uber_pickups/sep_noite_fds.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Setembro/14 - Noite - Fim de semana</figcaption>
                            <p>
                                Todos os mapas já apresentados só confirmam a magnitude da estação central de NYC, pois em todos eles a região da <i>Central Station</i> aparece entre os pontos com maior número de corridas iniciadas, ao lado do
                                <i>Rockfeller Center</i>, por exemplo.
                            </p>

                            <iframe src="medias/uber_pickups/sep_noite_semana.html" 
                                width="100%" height="500" frameborder="0" style="padding-bottom:0px; border-bottom:0px;" allowfullscreen="">
                            </iframe>
                            <figcaption style="font-size:70%">Setembro/14 - Noite - Semana</figcaption>
                            <p>
                                Por fim, os projetos de <i>Data Visualization</i> permitem consolidar uma enorme quantidade de dados para melhor visualização e tomadas de decisão. Conforme já reportado, apesar de não ter sido possível apresentar os 
                                dados com a dinâmica temporal, a finalidade do presente projeto foi mostrar as inúmeras possibilidades de análises que podem ser feitas com dados de geolocalização, além das diversas abordagens que podem ser tomadas 
                                em relação a esse tipo de dados.
                            </p>
                        
