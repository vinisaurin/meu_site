                    <h2> A crise dos combustíveis</h2>
                    <time>08/01/2020</time>
                    <article>
                        <p>
                            A alta dos preços do petróleo devido aos desentendimentos entre EUA e Irã combinada com
                            a mudança na política de preços da petrobrás trás à tona novamente, um ano e meio depois, 
                            o temor por uma nova greve dos caminhoneiros, a qual afetou de diferentes formas milhões
                            de pessoas no país.
                        </p>
                        <p>
                            O objetivo deste projeto é entender qual foi o comportamento dos preços dos combustíveis em 
                            geral por meio da análise de dados coletados semanalmente pela Agência Nacional do Petróleo (ANP)
                            em todos os estados do país. A amostra avaliada apresenta os dados de 2012 a Jun/2019.
                        </p>
                        <h3>Análise Descritiva</h3>
                        <p>
                            Em primeiro lugar, para conhecer os dados é apresentado um box-plot da distribuição dos preços
                            por tipo de combustível. Vale ressaltar que o preço do GLP, diferentemente dos outros
                            combustíveis que são medidos em litro, é mensurado por 13Kg.
                            <img src="medias/combustiveis/boxplot_medias.png" alt="Box-plot">

                            Pode-se perceber que o GLP apresenta a maior amplitude entre todos os combustíveis, o GNV 
                            possui todos os quartis inferiores aos demais, enquanto que a Gasolina possui todos os quartis
                            superiores aos demais.
                        </p>
                        <p>
                            A seguir, apresenta-se o gráfico de número de postos observados por Estado. É possível perceber que
                            com exceção do GNV, os demais combustíveis tem número de observações por Estado muito semelhante, e por
                            este motivo em algumas análises o GNV será excluído.
                            <img src="medias/combustiveis/observacoes_por_estado.png" alt="Número de observações por Estado">
                        </p>
                        <p>
                            Por último, abaixo está exposto o preço médio de todos os combustíveis por Estado no período analisado.
                            Uma rápida conclusão fácil de verificar é que o Estado de São Paulo possui um dos menores preços para 
                            todos os combustíveis e que a região Norte e Nordeste sofre mais com os preços altos de determinados combustíveis. 
                            Uma razão plausível para este cenário é de que há um custo de logística envolvido no preço e que as regiões mais distantes dos
                            grandes centros acabam suportando um custo mais alto.
                            <img src="medias/combustiveis/preco_med_estado.png" alt="Preço médio por Estado">
                        </p>


                        <h3>Análise Exploratória</h3>
                        <p>
                            A greve dos caminhoneiros no Brasil em 2018, também chamada de Crise do Diesel, foi uma paralisação de caminhoneiros autônomos 
                            com extensão nacional iniciada no dia 21 de maio, durante o governo de Michel Temer, e terminou oficialmente no dia 30 
                            de maio, com a intervenção de forças do Exército Brasileiro e Polícia Rodoviária Federal para desbloquear as rodovias.Os grevistas 
                            se manifestaram contra os reajustes frequentes e sem previsibilidade mínima nos preços dos combustíveis, principalmente do óleo diesel, 
                            realizados pela estatal Petrobras com frequência diária, pelo fim da cobrança de pedágio por eixo suspenso e pelo fim do PIS/Cofins sobre o diesel.
                        </p>
                        <p>
                            Conforme o gráfico abaixo, o a média nacional do preço de todos os combustíveis estava crescendo até 2016, mas este crescimento era freado pela política
                            escolhida pelo governo. Para tentar diminuir o crescimento da inflação no país, o governo utilizava a Petrobras como instrumento de 
                            política macroeconômica para controlar a inflação. A prática de controlar e atrasar o repasse dos preços internacionais aos combustíveis
                            no mercado interno permitia ao governo, na prática, influenciar os índices de inflação por meio da gasolina e do diesel.
                        </p>
                        <p>
                            Em 2016, Pedro Parente, novo presidente da Petrobras, foi nomeado por Michel Temer afirmando que a política de preços passaria a ser
                            guiada pelos interesses da empresa, sem influênica do governo. Por consequência, em Outubro do mesmo ano, o valor dos combustíveis começou 
                            a acompanhar a tendência do mercado internacional tomando por base não somente o preço do petróleo bruto, como também outros custos. Então,
                            em 2017 houve um dos maiores aumentos no preço do combustível desencadeando a Crise do Diesel já citada.
                            <img src="medias/combustiveis/evolucao_preco_nacional_2012_19.png" alt="Evolução dos preços dos combustíveis de 2012 a 2019">
                        </p>
                        <p>
                            Nas duas figuras seguintes, pode-se verificar que o aumento no preço dos combustíveis não é homogêneo no país inteiro. Enquanto os Estados do
                            Sul e Sudeste sofreram menos com os aumentos no preço dos combustíveis automotivos e do GLP, foram os Estados que mais suportaram aumento no
                            preço do GNV. Da mesma forma, os Estados do Centro-Oeste tiveram poucas complicações com o preço dos combustíveis líquidos, enquanto que foi a
                            segunda região do país com maior aumento no GLP, o qual é muito importante dentro da casa de todo brasileiro.
                            <img src="medias/combustiveis/alt_pct_prod_reg.png" alt="Alteração do preço por Macrorregião">
                        </p>
                        <p>
                            Dito tudo isso sobre o período desde 2012, agora este relatório terá um foco maior no período 2018 e 2019, o qual envolve o período da greve e os meses seguintes.
                            Na representação a seguir, o crescimento do preço de todos os combustíveis, com exceção do GNV, é muito homogêneo, mas também é de fácil visualização 
                            que os preços foram reduzidos momentaneamente, e após os 60 dias de redução de R$0,46 no litro do diesel declarado pelo ex-presidente Michel Temer, os
                            preços voltaram a subir e muito, ficando em patamares mais altos do que o atingido no mês da greve. 
                            <img src="medias/combustiveis/preco_comb_201819_regiao.png" alt="Evolução dos preços por Macrorregião em 2018 e 2019">
                        </p>
                        <p>
                            Entretanto, novamente em outubro de 2018 os preços voltaram a ter uma redução motivada principalmente pela aprovação da Lei 13.273/18, a qual aprovava a subvenção 
                            para a venda e a importação do óleo diesel, dada anteriormente pelo governo, por meio de medida provisória. Voltando a entrar numa tendência altista no primeiro mês 
                            do ano de 2019, o que volta a colocar no foco dos caminhoneiros e governo, a possibilidade de uma nova greve.
                        </p>
                        <p>
                            Nesta nova representação, pode-se visualizar que quase na totalidade do país o combustível que teve menor aumento percentual foi o GLP, com apenas 6 exceções, sendo
                            que SP, PR e MG tiveram o álcool como o combustível que teve menor crescimento no preço. Do lado direito, o cenário é mais heterogêneo, uma significante parte dos Estados 
                            da região Nordeste tiveram o álcool com maior aumento no preço e nos Estados de SP, PR e SC o maior vilão foi o Diesel.
                            <img src="medias/combustiveis/maior_menor_aumento.png" alt="Combustíveis com maior e menor alteração percentual">
                        </p>
                        <p>
                            Na figura a seguir confirma-se o que já foi dito anteriormente, mas agora é possível visualizar quais Estados suportaram maiores ou menores aumentos para cada combustível.
                            Em alguns casos, como por exemplo o Diesel e Diesel S10 no Mato Grosso do Sul, tiveram redução do preço, na mesma medida que o Estado do Maranhão viu estes mesmos combustíveis
                            aumentarem o preço em mais de 10%.
                            <img src="medias/combustiveis/alt_prc_estado_18_19.png" alt="Alteração percentual no preço nos anos de 2018 e 2019">
                        </p>
                        <p>
                            Uma questão suplementar a este trabalho é verificar como é a relação dos preços do álcool e gasolina em cada Estado do país, já que hoje 67,1% da frota brasileira é de carros <i>flex</i>. 
                            A maioria das montadoras indica que se o preço do álcool for inferior a 70% do preço da gasolina, é mais vantajoso usar o combustível renovável, caso contrário é mais vantajoso usar o 
                            combustível fóssil. No gráfico abaixo, verifica-se que apenas poucos Estados possuem uma boa relação para a utilização do álcool, o que torna o país ainda mais dependente da Gasolina e do
                            petróleo.
                            <img src="medias/combustiveis/rel_alcool_gasolina.png" alt="Relação álcool/gasolina nos Estados brasileiros">
                        </p>
                    </article>
